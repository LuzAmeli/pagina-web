		<h3>Alimentación para gatos de 4-8 semanas</h3>
		<p>Los gatitos suelen comenzar el proceso de destete alrededor de la cuarta semana. Durante este tiempo, comenzarán a pasar gradualmente de 
		la leche o fórmula a una dieta de alimentos sólidos, que les aportan proteínas, ácidos grasos y nutrientes que impulsan su desarrollo inicial.
		De 4 a 5 semanas de edad, los gatitos alimentados con biberón aún necesitan leche en polvo para reemplazar la leche, pero también puedes comenzar
		a hacer una transición gradual a una dieta de comida para gatitos diluida en agua. Comienza reemplazando algunas de las comidas habituales del 
		gatito con una papilla suelta de comida húmeda para gatitos y leche en polvo para gatitos en una cuchara. Luego, comienza a alimentar gradualmente 
		a tu gatito con un platillo poco profundo. Una vez que el gatito tenga entre 5 y 6 semanas de edad y coma bien la comida para gatitos, puedes dejar 
		de alimentarlo con biberón.</p>
		</article>
