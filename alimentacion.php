		<h3>Alimentación para gatos de 6 meses a 10 años</h3>
		<p>Los gatitos mayores todavía necesitan muchas calorías para impulsar su crecimiento, pero su metabolismo comenzará a disminuir gradualmente y sus necesidades
		nutricionales empezarán a parecerse más a las de un adulto. Cuando tu gatito cumpla su primer año, puedes empezar a hacer la transición de la comida para gatitos
		a una dieta para adultos. Sin embargo, ten en cuenta que los gatos de razas grandes pueden seguir creciendo hasta los 3 o 4 años y pueden seguir comiendo una dieta 
		orientada al crecimiento.A la hora de decidir cuánto alimentar a tu gato, tendrás que tener en cuenta su raza, edad, estado reproductivo, problemas de salud subyacentes
		y más. No obstante, en general, la ingesta calórica diaria recomendada es de unas 20 calorías por cada libra de peso corporal. </p>
		</article>
