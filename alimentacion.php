		<h3>Alimentación para gatos de 4-6 meses</h3>
		<p>Durante esta etapa, tu gatito comenzará a adaptarse a su rutina alimentaria. Por eso es fundamental que te asegures de establecer buenos hábitos para 
		la edad adulta. Alimentarlo con una dieta variada puede evitar que se vuelva quisquilloso y mantenerse mentalmente estimulado.Los alimentos húmedos o enlatados
		suelen ser ricos en proteínas de origen animal apropiadas para la especie, tienen un menor contenido de carbohidratos y un mayor contenido de humedad (70 % a 80 %)
		que el de los alimentos secos (6 % a 10 %).Los gatitos de este grupo de edad necesitan alrededor de 60 a 65 calorías por libra de peso corporal por día.</p>
		</article>
