		<h3>Alimentación para gatos de 0-4 semanas</h3>
		<p>Durante esta etapa, la mayoría de los gatitos dependen de la leche materna como única fuente de nutrición. Si la madre está presente,
		no necesitarás alimentarlos en absoluto: ¡ellos sabrán cómo alimentarse por sí mismos! Dicho esto, si has rescatado a un gatito huérfano, 
		tendrás que alimentarlo con biberón.Los gatitos necesitan una fórmula de reemplazo de leche para gatitos, que replica la nutrición que se 
		encuentra en la leche de la gata madre. Puedes comprar un sustituto de leche para gatitos en tu veterinario o en una tienda de artículos
		para mascotas. No alimentes a un gatito con leche de vaca, ya que no tiene el equilibrio nutricional adecuado para nutrir a un gatito recién nacido.</p>
