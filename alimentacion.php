		<h3>Alimentación para gatos de 8-16 semanas</h3>
		<p>Entre las 8 y las 10 semanas de edad, el gatito está completamente destetado y debería estar comiendo un alimento para gatitos a base de carne
		que le aporte abundantes proteínas, ácidos grasos de origen animal para el desarrollo del cerebro y los ojos, y los niveles adecuados de vitaminas
		y minerales.Durante esta fase, tu gatito se desarrolla rápidamente y necesita muchas calorías para sostener ese crecimiento. Los gatitos en crecimiento
		pueden necesitar entre 250 y 280 calorías por día, y las razas más grandes, como los Maine Coon y los Ragdoll, requieren hasta 360 calorías diarias.Lo 
		ideal es que coma de tres a cuatro veces al día, pero los gatitos mayores de ocho semanas también pueden alimentarse libremente con alimento seco. Ten
		cuidado con la alimentación libre. Si bien tu gatito debería estar aumentando de peso en esta etapa, demasiada comida seca puede provocar un aumento
		excesivo de peso.</p>
		</article>
