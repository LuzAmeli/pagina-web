		<article>
		<h3>Alimetación para gatos de 11 años o más</h3>
		<p>En comparación con los adultos jóvenes y de mediana edad, los gatos mayores tienen necesidades nutricionales particulares. Suelen presentar una capacidad reducida
		para metabolizar proteínas y, por lo tanto, tienden a perder masa muscular.Los gatos mayores necesitan proteínas más digeribles para mantener una masa muscular magra
		y mantenerse sanos. Los gatos de esta edad también pueden desarrollar artritis y otras afecciones inflamatorias, por lo que los ácidos grasos omega-3 son un complemento
		especialmente beneficioso para la dieta de los gatos mayores.Dado que los gatos mayores pueden tener dificultades para comer debido a problemas dentales, la comida húmeda
		es mejor para los gatos mayores.Los gatos mayores y ancianos necesitan más calorías por cada kilo de peso corporal. Si parece que tu gato mayor está perdiendo masa
		muscular, considera aumentar su ingesta calórica a 30 o 40 calorías por cada kilo. La mayoría de esas calorías deben provenir de proteínas de origen animal, lo que ayuda
		a prevenir la sarcopenia (pérdida de masa muscular debido al envejecimiento).</p>
